const ALL_ZONES = ["net-front","slot","high-slot","left-faceoff","right-faceoff","left-corner","right-corner","behind-net","left-boards","right-boards","left-point","right-point"];
const otherZones = (correct) => ALL_ZONES.filter(z => !correct.includes(z));

// Compact helpers — choice verdicts shorthand, keeps data dense.
const C = (text, feedback) => ({ text, verdict: "correct", feedback });
const P = (text, feedback) => ({ text, verdict: "partial", feedback });
const W = (text, feedback) => ({ text, verdict: "wrong", feedback });

// A minimal ZC (zone-click) builder: correct array + partial array (everything
// else becomes wrong). Keeps the feedback triplet required.
function zc(prompt, correct, partial, feedback) {
  const all = [...correct, ...partial];
  return {
    mode: "zone-click",
    prompt,
    zones: {
      correct,
      partial,
      wrong: ALL_ZONES.filter(z => !all.includes(z)),
    },
    feedback,
  };
}

// A choice-mode question. options: [{text, verdict, feedback}].
function choice(prompt, options) {
  return { mode: "choice", prompt, options };
}

